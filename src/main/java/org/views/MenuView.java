    private void createUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserButtonActionPerformed
        if(!userTextField.getText().isBlank()) {
            String inputText = userTextField.getText();
            int result = db.signUp(inputText.trim());
            
            if(result == 0) {
                JOptionPane.showMessageDialog(null, "¡Usuario '" + inputText.trim() +"' creado! \nAhora puede acceder a su biblioteca");
            } else {
                JOptionPane.showMessageDialog(null, "Error: El usuario ya existe", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error: Escriba el nombre del usuario","Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_createUserButtonActionPerformed

