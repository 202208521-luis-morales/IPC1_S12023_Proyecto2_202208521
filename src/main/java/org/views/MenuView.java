    private void signInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInButtonActionPerformed
       if(!userTextField.getText().isBlank()) {
           String inputText = userTextField.getText();
           
           if(db.logIn(inputText) == 0) {
               LibraryView lb = new LibraryView();
               lb.setVisible(true);
               this.dispose();
           } else {
               JOptionPane.showMessageDialog(null, "Error: El usuario no fue encontrado","Error", JOptionPane.ERROR_MESSAGE);
           }
       } else {
           JOptionPane.showMessageDialog(null, "Error: Escriba el nombre del usuario","Error", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_signInButtonActionPerformed

